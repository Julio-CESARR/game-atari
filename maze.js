export function getTile(col, row) {
    if (row < 0 || row >= MAZE.length) return 0;
    if (col < 0 || col >= MAZE[0].length) return 0;
    return MAZE[row][col];
}

export function isWall(col, row) {
    const tile = getTile(col, row);
    return tile === 1;
}

export function isGate(col, row) {
    const tile = getTile(col, row);
    return tile === 4;
}
